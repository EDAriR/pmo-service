package com.syntrontech.pmo.model.model.to;

public class AbnormalTO {
	
	private String unitName;
	private int notYetNumber;
	private int diagnosisNormalNumber;
	private int diagnosisAbnormalityNumber;
	private int refuseDiagnosisNumber;
	private int notContactNumber;
	
	public String getUnitName() {
		return unitName;
	}
	public void setUnitName(String unitName) {
		this.unitName = unitName;
	}
	public int getNotYetNumber() {
		return notYetNumber;
	}
	public void setNotYetNumber(int notYetNumber) {
		this.notYetNumber = notYetNumber;
	}
	public int getDiagnosisNormalNumber() {
		return diagnosisNormalNumber;
	}
	public void setDiagnosisNormalNumber(int diagnosisNormalNumber) {
		this.diagnosisNormalNumber = diagnosisNormalNumber;
	}
	public int getDiagnosisAbnormalityNumber() {
		return diagnosisAbnormalityNumber;
	}
	public void setDiagnosisAbnormalityNumber(int diagnosisAbnormalityNumber) {
		this.diagnosisAbnormalityNumber = diagnosisAbnormalityNumber;
	}
	public int getRefuseDiagnosisNumber() {
		return refuseDiagnosisNumber;
	}
	public void setRefuseDiagnosisNumber(int refuseDiagnosisNumber) {
		this.refuseDiagnosisNumber = refuseDiagnosisNumber;
	}
	public int getNotContactNumber() {
		return notContactNumber;
	}
	public void setNotContactNumber(int notContactNumber) {
		this.notContactNumber = notContactNumber;
	}
	
}
